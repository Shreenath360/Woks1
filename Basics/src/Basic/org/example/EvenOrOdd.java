package Basic.org.example;

public class EvenOrOdd {

	public static void main(String[] args) {
    int num=13;
  for(int i=1;i<=num;i++);
  {
	  if(num%2 ==0)
	  {
		  System.out.println("the number is Even");
	  }
	  else
	  {
		  System.out.println("the number is odd");
	  }
  }

}
}